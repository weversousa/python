# Coerção automática signifa que Python é de Tipagem Fraca.
# Tipagem Fraca é quando uma variável pode receber qualquer tipo de valor.


# Exemplos:

numero = 3
print(numero)  # 3

numero = 'Paula'
print(numero)  # Paula

numero = True
print(numero)  # True

numero = ['a', 'e']
print(numero)  # ['a', 'e']

numero = 'Olá!'
print(numero)  # Olá!
