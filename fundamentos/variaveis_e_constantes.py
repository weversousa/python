# Não precisa declarar o tipo.
nome = 'Weverton'
idade = 30
casado = False
print(nome, idade, casado)

# Resultado:
# Weverton 30 False
# -----------------------------------------------------------------------------

# Tipagem dinâmica.
nome = 12
print(nome, idade, casado)

# Resultado:
# 12 30 False
# -----------------------------------------------------------------------------

# A Constante por convenção é declarada em letras maiúsculas.
PI = 3.14
print(PI)

# Resultado:
# 3.14
