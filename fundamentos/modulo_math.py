from math import sqrt, pow, ceil, floor

# Funções matemáticas da biblioteca "math"
# https://www.programiz.com/python-programming/modules/math

raiz_quadrada = sqrt(49)
print(raiz_quadrada)

# Resultado:
# 7.0
# -----------------------------------------------------------------------------

exponenciacao = pow(2, 3)
print(exponenciacao)

# Resultado:
# 8.0
# -----------------------------------------------------------------------------

arredonda_para_cima = ceil(7.1)
print(arredonda_para_cima)

# Resultado:
# 8

arredonda_para_baixo = floor(7.9)
print(arredonda_para_baixo)

# Resultado:
# 7
